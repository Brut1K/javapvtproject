package by.it_academy.lesson5.part1.Sharp;

public class Rectangle extends Shape {
    private double a;
    private double b;

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    @Override
    public double getSqr() {
        return a*b;
    }
}
